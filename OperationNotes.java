public class OperationNotes {
  public static void main(String[] args){
    System.out.println(2 + 3); // addition 
    System.out.println(2 - 3); // subtraction 
    System.out.println(2 * 3); // multiplication
    System.out.println(2/3); // division
    System.out.println(7%3); // remainder, modulo, mod
    // compound opperators

    int x = 5;
    x += 1; // add 1 to the variable
    System.out.println(x);
    x++; // another wat to add 1; increment 
    System.out.println(x);
    x--; // subtract 1, decrement
    System.out.println(x);
    x*= 6;
    System.out.println(x);
}
}
